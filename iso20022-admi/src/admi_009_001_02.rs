// RequestDetails3 ...
#[derive(Debug, Default, PartialEq, Clone, Serialize, Deserialize)]
pub struct RequestDetails3 {
	#[serde(rename = "Tp")]
	pub tp: Max35Text,
	#[serde(rename = "Key", skip_serializing_if = "Option::is_none")]
	pub key: Option<Max35Text>,
}

impl RequestDetails3 {
	pub fn validate(&self) -> bool {
		if !self.tp.validate() { return false }
		if let Some(ref key_value) = self.key { if !key_value.validate() { return false; } }
		return true
	}
}


// StaticDataRequestV02 ...
#[derive(Debug, Default, PartialEq, Clone, Serialize, Deserialize)]
pub struct StaticDataRequestV02 {
	#[serde(rename = "MsgId")]
	pub msg_id: Max35Text,
	#[serde(rename = "SttlmSsnIdr", skip_serializing_if = "Option::is_none")]
	pub sttlm_ssn_idr: Option<Exact4AlphaNumericText>,
	#[serde(rename = "DataReqDtls")]
	pub data_req_dtls: RequestDetails3,
	#[serde(rename = "SplmtryData", skip_serializing_if = "Option::is_none")]
	pub splmtry_data: Option<Vec<SupplementaryData1>>,
}

impl StaticDataRequestV02 {
	pub fn validate(&self) -> bool {
		if !self.msg_id.validate() { return false }
		if let Some(ref sttlm_ssn_idr_value) = self.sttlm_ssn_idr { if !sttlm_ssn_idr_value.validate() { return false; } }
		if !self.data_req_dtls.validate() { return false }
		if let Some(ref splmtry_data_vec) = self.splmtry_data { for item in splmtry_data_vec { if !item.validate() { return false; } } }
		return true
	}
}


