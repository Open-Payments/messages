	// Event1 ...
	#[cfg_attr(feature = "derive_debug", derive(Debug))]
	#[cfg_attr(feature = "derive_default", derive(Default))]
	#[cfg_attr(feature = "derive_serde", derive(Serialize, Deserialize))]
	#[cfg_attr(feature = "derive_clone", derive(Clone))]
	#[cfg_attr(feature = "derive_partial_eq", derive(PartialEq))]
	pub struct Event1 {
		#[cfg_attr( feature = "derive_serde", serde(rename = "EvtCd") )]
		pub evt_cd: Max4AlphaNumericText,
		#[cfg_attr( feature = "derive_serde", serde(rename = "EvtParam", skip_serializing_if = "Option::is_none") )]
		pub evt_param: Option<Vec<Max35Text>>,
		#[cfg_attr( feature = "derive_serde", serde(rename = "EvtDesc", skip_serializing_if = "Option::is_none") )]
		pub evt_desc: Option<Max350Text>,
		#[cfg_attr( feature = "derive_serde", serde(rename = "EvtTm", skip_serializing_if = "Option::is_none") )]
		pub evt_tm: Option<String>,
	}
	
	impl Event1 {
		pub fn validate(&self) -> Result<(), ValidationError> {
			if let Err(e) = self.evt_cd.validate() { return Err(e); }
			if let Some(ref evt_param_vec) = self.evt_param { for item in evt_param_vec { if let Err(e) = item.validate() { return Err(e); } } }
			if let Some(ref evt_desc_value) = self.evt_desc { if let Err(e) = evt_desc_value.validate() { return Err(e); } }
			Ok(())
		}
	}
	
	
	// Exact4AlphaNumericText ...
	#[cfg_attr(feature = "derive_debug", derive(Debug))]
	#[cfg_attr(feature = "derive_default", derive(Default))]
	#[cfg_attr(feature = "derive_serde", derive(Serialize, Deserialize))]
